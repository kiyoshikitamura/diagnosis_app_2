# Skip header
start_idx = 1
for line in lines[start_idx:]:
    if not line.strip():
        continue
    
    parts = line.split(',')
    # We expect 4 parts. If result text or ad title has commas, we need to join.
    # But based on inspection, commas are not present in text, only Ideographic Comma (、).
    # However, just in case, let's allow for 4 parts minimum.
    # Actually, let's treat the first 2 as fixed, and the last 1 as fixed (Ad Title). The middle is Result Text.
    # WAIT, Ad Title might have commas too? "【...】...,..."? 
    # Example: "【教養】一人の時間を最高に充実させる、プロ厳選の知的ホビー特集" -> "、" is used.
    # Example: "【年収1000万超】あなたの決断力を最高評価するヘッドハンティング" -> No comma.
    # It seems safe to simple split.
    
    if len(parts) < 4:
        print(f"Skipping line (parts < 4): {line}")
        continue
    
    # Simple split might be dangerous if there ARE ascii commas.
    # Let's hope not.
    
    animal_ja = parts[0]
    attribute_id = parts[1]
    result_text = parts[2]
    ad_title = parts[3]
    
    # If there are more parts, it means accidental split.
    # But for now, let's assume 4.
    if len(parts) > 4:
        # Reconstruct middle
        # Only result_text likely to contain commas if any.
        # But wait, ad_title is the last part.
        result_text = ",".join(parts[2:-1])
        ad_title = parts[-1]
    
    animal_en = animal_name_map.get(animal_ja)
